#include "3-calc.h"
/**
 * op_add - calculates the sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtrates two integers
 * @a: first integer
 * @b: second integer
 * Return: difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: results
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the modulus
 * @a: first integer
 * @b: second integer
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
